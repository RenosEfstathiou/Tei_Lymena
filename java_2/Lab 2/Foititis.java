class Foititis
{
    private String onoma;
    private String eponumo;
    private int am;
    private int etos;

    Foititis(String onoma, String eponumo, int am, int etos)
    {
        this.onoma = onoma;
        this.eponumo = eponumo;
        this.am = am;
        this.etos = etos;
    }

    public String getOnoma()
    {
        return onoma;
    }

    public String getEponumo()
    {
        return eponumo;
    }

    public int getAm()
    {
        return am;
    }

    public int getEtos()
    {
        return etos;
    }
}
