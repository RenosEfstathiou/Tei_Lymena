class Ypallilos
{
    private String onoma, dieuthinsi;
    private int AM;

    Ypallilos(String onoma, String dieuthinsi, int AM)
    {
        this.onoma = onoma;
        this.dieuthinsi = dieuthinsi;
        this.AM = AM;
    }

    public String getOnoma()
    {
        return onoma;
    }

    public String getDieuthinsi()
    {
        return dieuthinsi;
    }

    public int getAM()
    {
        return AM;
    }
}
